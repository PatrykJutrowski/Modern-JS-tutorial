var x = 5, y = 10;

var z = x + y; //performs addition and returns 15
document.write(z + " Addition"+"<br>")
z = y - x; //performs subtraction and returns 5
document.write(z + " Subtraction"+ "<br>")
z = x * y; //performs multiplication and returns 50
document.write(z + " Multiplication"+ "<br>")
z = y / x; //performs division and returns 2
document.write(z + " Division"+ "<br>")
z = x % 2; //returns division remainder 1
document.write(z + " division remainder"+ "<br>")
z = x ** y; // Exponentiation
document.write(z + " Exponentiation"+ "<br>")