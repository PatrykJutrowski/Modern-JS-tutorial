const hero = {
    name: 'Batman',
    city: 'Gotham'
};
Object.keys(hero); // => ['name', 'city']


const hero1 = {
    name1: 'Batman',
    city1: 'Gotham'
};
Object.values(hero1); // => ['Batman', 'Gotham']

const hero2 = {
    name2: 'Batman',
    city2: 'Gotham'
};
Object.entries(hero2); // => `[['name', 'Batman'], ['city', 'Gotham']]`